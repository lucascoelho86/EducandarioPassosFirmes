package br.com.educandariopassosfirmes.entidades;

public class DisciplinaTipoEnsino {
	private Integer idDisciplina;
	private Integer cdTipoEnsino;
	
	public Integer getIdDisciplina() {
		return idDisciplina;
	}
	public void setIdDisciplina(Integer idDisciplina) {
		this.idDisciplina = idDisciplina;
	}
	public Integer getCdTipoEnsino() {
		return cdTipoEnsino;
	}
	public void setCdTipoEnsino(Integer cdTipoEnsino) {
		this.cdTipoEnsino = cdTipoEnsino;
	}
	
}
